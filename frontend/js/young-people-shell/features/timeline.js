function renderNoContext() {
  if (!els.viewContent) return;

  const message =
    getCurrentScope() === "child"
      ? "Select a young person before opening timeline."
      : "A home context is needed before timeline can load.";

  els.viewContent.innerHTML = `
    <div class="empty-state">
      <p>${toText(message)}</p>
    </div>
  `;

  updateWorkspaceSummaryStrip({
    today: "No timeline context",
    nextEvent: "No event loaded",
    lastRecord: "No timeline data",
    openActions: "No actions loaded",
  });
}

async function safeList(recordType, ids = {}) {
  try {
    return await listRecords(recordType, ids);
  } catch (error) {
    console.warn(`[timeline] failed to load ${recordType}`, error);
    return [];
  }
}

async function safeGetRows(url = "") {
  try {
    const response = await apiGet(url, { skipCache: true });

    if (Array.isArray(response)) return response;
    if (Array.isArray(response.items)) return response.items;
    if (Array.isArray(response.records)) return response.records;
    if (Array.isArray(response.results)) return response.results;
    if (Array.isArray(response.data)) return response.data;

    return [];
  } catch (error) {
    console.warn(`[timeline] failed to load ${url}`, error);
    return [];
  }
}

async function loadChildTimeline(youngPersonId) {
  const ids = { youngPersonId };

  const [
    chronology,
    incidents,
    dailyNotes,
    healthRecords,
    educationRecords,
    familyRecords,
    appointments,
    missingEpisodes,
    safeguardingRecords,
    tasks,
    documents,
    statutoryDocuments,
    medicationRecords,
    handoverRecords,
    riskRecords,
    keyworkRecords,
  ] = await Promise.all([
    safeList("chronology_event", ids),
    safeList("incident", ids),
    safeList("daily_note", ids),
    safeList("health_record", ids),
    safeList("education_record", ids),
    safeList("family_contact", ids),
    safeList("appointment", ids),
    safeList("missing_episode", ids),
    safeList("safeguarding_record", ids),
    safeList("task", ids),
    safeList("document", ids),
    safeList("statutory_document", ids),
    safeList("medication_record", ids),
    safeList("handover_record", ids),
    safeList("risk", ids),
    safeList("keywork", ids),
  ]);

  const fallback = [
    ...incidents,
    ...dailyNotes,
    ...healthRecords,
    ...educationRecords,
    ...familyRecords,
    ...appointments,
    ...missingEpisodes,
    ...safeguardingRecords,
    ...tasks,
    ...documents,
    ...statutoryDocuments,
    ...medicationRecords,
    ...handoverRecords,
    ...riskRecords,
    ...keyworkRecords,
  ];

  const timelineRows = sortNormalisedRecordsNewestFirst(
    chronology.length ? chronology : fallback
  );

  return {
    chronology,
    timelineRows,
  };
}

async function loadHomeTimeline(homeId) {
  const urls = [
    `/homes/${homeId}/inspection-scores`,
    `/homes/${homeId}/quality-audits`,
    `/homes/${homeId}/quality-audit-findings`,
    `/homes/${homeId}/quality-audit-actions`,
    `/homes/${homeId}/compliance-items`,
    `/homes/${homeId}/reg44-visits`,
    `/homes/${homeId}/reg44-findings`,
    `/homes/${homeId}/reg44-actions`,
    `/homes/${homeId}/reg45-reviews`,
    `/homes/${homeId}/reg45-actions`,
    `/homes/${homeId}/inspection-section-scores`,
    `/homes/${homeId}/inspection-score-reasons`,
    `/homes/${homeId}/inspection-lines-of-enquiry`,
    `/homes/${homeId}/inspection-improvement-actions`,
    `/homes/${homeId}/manager-review-queue`,
    `/homes/${homeId}/team`,
    `/homes/${homeId}/supervisions`,
    `/homes/${homeId}/training`,
    `/homes/${homeId}/tasks`,
    `/homes/${homeId}/documents`,
    `/homes/${homeId}/reports`,
    `/homes/${homeId}/incidents`,
    `/homes/${homeId}/safeguarding`,
  ];

  const groups = await Promise.all(urls.map(safeGetRows));

  const rows = groups
    .flat()
    .map((row) => ({
      ...row,
      home_id: row.home_id || homeId,
    }));

  const timelineRows = sortNormalisedRecordsNewestFirst(normaliseRecords(rows));

  return {
    chronology: [],
    timelineRows,
  };
}

function bindTimelineRowEvents(records = []) {
  if (!els.viewContent) return;

  const byKey = new Map();

  records.forEach((record) => {
    const key = `${record.type || record.record_type || ""}:${record.id || ""}`;
    byKey.set(key, record);
  });

  els.viewContent.querySelectorAll("[data-open-record='true']").forEach((row) => {
    const open = () => {
      const type = row.getAttribute("data-record-type") || "";
      const id = row.getAttribute("data-record-id") || "";
      const record = byKey.get(`${type}:${id}`);

      if (record) {
        openRecordDetail(record);
      }
    };

    row.addEventListener("click", open);
    row.addEventListener("keydown", (event) => {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        open();
      }
    });
  });
}

export async function loadTimeline() {
  if (!els.viewContent) return;

  const scope = getCurrentScope();
  const id = getScopeEntityId();

  if (!id) {
    renderNoContext();
    return;
  }

  els.viewContent.innerHTML = `
    <div class="loading-state">
      <div>
        <div class="spinner"></div>
        <p>Loading timeline...</p>
      </div>
    </div>
  `;

  try {
    const result =
      scope === "child" ? await loadChildTimeline(id) : await loadHomeTimeline(id);

    const timelineRows = sortNormalisedRecordsNewestFirst(
      normaliseRecords(result.timelineRows)
    );

    const chronology = sortNormalisedRecordsNewestFirst(
      normaliseRecords(result.chronology)
    );

    const importantRows = buildRecentImportantRows(timelineRows).slice(0, 10);
    const buckets = buildCategoryBuckets(timelineRows);
    const patternCounts = buildPatternCounts(timelineRows);
    const topCounts = buildTopCounts({ timelineRows });

    els.viewContent.innerHTML = renderTimelineHtml({
      chronology,
      timelineRows,
      importantRows,
      buckets,
      patternCounts,
      topCounts,
    });

    bindTimelineRowEvents(timelineRows);

    const latest = timelineRows[0] || null;
    const nextAttention = importantRows[0] || null;

    updateWorkspaceSummaryStrip({
      today: `${timelineRows.length} timeline item${timelineRows.length === 1 ? "" : "s"}`,
      nextEvent: nextAttention
        ? `${nextAttention.title || "Priority event"}`
        : "No urgent timeline event",
      lastRecord: latest?.date
        ? `Latest ${formatTimelineDate(latest.date)}`
        : "No recent timeline record",
      openActions: `${importantRows.length} important event${
        importantRows.length === 1 ? "" : "s"
      }`,
    });
  } catch (error) {
    els.viewContent.innerHTML = `
      <div class="empty-state">
        <p>${escapeHtml(error.message || "Failed to load timeline.")}</p>
      </div>
    `;

    updateWorkspaceSummaryStrip({
      today: "Timeline unavailable",
      nextEvent: "Unable to load",
      lastRecord: "No timeline data",
      openActions: "Check API routes",
    });
  }
}

export const loadCurrentView = loadTimeline;

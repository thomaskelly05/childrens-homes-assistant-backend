window.onload = () => {

  if (window.initChat) {
    window.initChat();
  }

  if (window.initAssistantMeetingModal) {
    window.initAssistantMeetingModal();
  }

  if (window.createConversation) {
    window.createConversation();
  }

  if (window.loadConversations) {
    window.loadConversations();
  }

};

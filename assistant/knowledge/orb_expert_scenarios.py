"""Gold expert stress-test scenarios for ORB residential practice."""

from __future__ import annotations

from typing import Any

from assistant.knowledge.orb_expert_scenario_families import ORB_SCENARIO_FAMILIES

_FAMILY_BY_ID = {f["id"]: f for f in ORB_SCENARIO_FAMILIES}


def _gold(
    scenario_id: str,
    title: str,
    family: str,
    role: str,
    risk_level: str,
    prompt: str,
    *,
    child_profile: list[str] | None = None,
    context_modifiers: list[str] | None = None,
    evidence_gaps: list[str] | None = None,
    expected_markers: list[str] | None = None,
    optional_markers: list[str] | None = None,
    must_not_say: list[str] | None = None,
    source_anchors: list[str] | None = None,
    expected_actions: list[str] | None = None,
    expected_recording_points: list[str] | None = None,
    expected_manager_oversight: list[str] | None = None,
    expected_reg44_questions: list[str] | None = None,
    expected_nvq_evidence: list[str] | None = None,
    output_modes_to_test: list[str] | None = None,
) -> dict[str, Any]:
    fam = _FAMILY_BY_ID.get(family) or {}
    default_anchors = list(fam.get("likely_source_anchors") or [])
    if not default_anchors:
        default_anchors = ["dfe_childrens_homes_regulations_guide", "working_together_safeguarding"]
    merged_anchors: list[str] = []
    for a in (source_anchors or []) + default_anchors:
        if a not in merged_anchors:
            merged_anchors.append(a)
    merged_must_not = list(_COMMON_UNSAFE)
    for m in must_not_say or []:
        if m not in merged_must_not:
            merged_must_not.append(m)
    return {
        "scenario_id": scenario_id,
        "title": title,
        "family": family,
        "role": role,
        "risk_level": risk_level,
        "prompt": prompt,
        "child_profile": child_profile or [],
        "context_modifiers": context_modifiers or [],
        "evidence_gaps": evidence_gaps or [],
        "expected_markers": expected_markers or [],
        "optional_markers": optional_markers or [],
        "must_not_say": merged_must_not,
        "source_anchors": merged_anchors,
        "expected_actions": expected_actions or [],
        "expected_recording_points": expected_recording_points or [],
        "expected_manager_oversight": expected_manager_oversight or [],
        "expected_reg44_questions": expected_reg44_questions or [],
        "expected_nvq_evidence": expected_nvq_evidence or [],
        "output_modes_to_test": output_modes_to_test or ["what_am_i_missing", "safeguarding_lens"],
        "generated": False,
        "needs_human_review": False,
        "generated_by": None,
        "source_scenario_id": None,
    }



    rows: list[dict[str, Any]] = []
    for category in CATEGORY_BENCHMARK_PACK:
        for prompt in category["prompts"]:
            rows.append(
                {
                    "category_id": category["category_id"],
                    "category_label": category["label"],
                    **prompt,
                }
            )
    return rows


